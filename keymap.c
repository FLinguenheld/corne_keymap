enum layers { 
    _BASE, 
    _NUMERIC, 
    _ARROWS,
    _MOUSE,
    _ADJUST,
    _FN,

    // US --
    _US_BASE,
    _US_NUMERIC,
    _US_ARROWS,
    _US_MOUSE,
    _US_ADJUST,
    _US_FN,
};

// Current language ?
int is_currently_in_us(void) {

    switch (get_highest_layer(layer_state)) {

        case _US_BASE:
        case _US_NUMERIC:
        case _US_ARROWS:
        case _US_MOUSE:
        case _US_ADJUST:
        case _US_FN:
            return true;

        default:
            return false;
    }
}

